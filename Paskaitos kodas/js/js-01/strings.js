const a = 'a';
console.log(a); // a
const b = 'b';
console.log(b); // b
const n = 'n';
console.log(n); // n
const ba = b + a;
console.log(ba); // ba
const na = n + a;
console.log(na); // na
const banana = ba + na + na;
console.log(banana); // banana

